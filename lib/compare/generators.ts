export function formatChangeSummary(comparison: SnapshotComparison): string {
  const { meta, visualDiff, uxComparison } = comparison

  return `
${meta.snapshotCount} snapshots compared
${meta.totalDifferences} total differences
${visualDiff.summary.totalChanges} visual changes
${uxComparison.flowChanges.length} flow changes
  `.trim()
}

export function getMajorChangesOnly(comparison: SnapshotComparison): any[] {
  return comparison.meta.majorChanges.filter(c => c.impact === 'high')
}

export function getHighImpactOpportunities(comparison: SnapshotComparison): any[] {
  return comparison.opportunities.filter(o => o.impact === 'high')
}

export function getTopRecommendations(comparison: SnapshotComparison, limit = 5): any[] {
  return comparison.recommendations
    .sort((a, b) => a.priority - b.priority)
    .slice(0, limit)
}
