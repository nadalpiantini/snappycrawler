export function generateComparisonMatrix(comparison: SnapshotComparison): ComparisonMatrix {
  const dimensions = defineComparisonDimensions()
  const scores = calculateDimensionScores(comparison, dimensions)

  return {
    dimensions,
    scores
  }
}

function defineComparisonDimensions(): ComparisonDimension[] {
  return [
    {
      name: 'Visual Complexity',
      weight: 0.8,
      category: 'visual'
    },
    {
      name: 'Design Consistency',
      weight: 0.9,
      category: 'visual'
    },
    {
      name: 'UX Flow Efficiency',
      weight: 1.0,
      category: 'ux'
    },
    {
      name: 'Interaction Quality',
      weight: 0.9,
      category: 'ux'
    },
    {
      name: 'Content Clarity',
      weight: 0.8,
      category: 'content'
    },
    {
      name: 'Messaging Effectiveness',
      weight: 0.7,
      category: 'content'
    },
    {
      name: 'Code Quality',
      weight: 0.9,
      category: 'technical'
    },
    {
      name: 'Performance',
      weight: 1.0,
      category: 'technical'
    }
  ]
}

function calculateDimensionScores(
  comparison: SnapshotComparison,
  dimensions: ComparisonDimension[]
): ComparisonScore[] {
  const scores: ComparisonScore[] = []

  dimensions.forEach(dimension => {
    comparison.meta.urls.forEach((_, index) => {
      const score = calculateScoreForDimension(comparison, dimension.name, index)
      scores.push({
        snapshotIndex: index,
        dimension: dimension.name,
        score: score.raw,
        normalizedScore: score.normalized,
        notes: score.notes
      })
    })
  })

  return scores
}

function calculateScoreForDimension(
  comparison: SnapshotComparison,
  dimensionName: string,
  snapshotIndex: number
): { raw: number; normalized: number; notes: string } {
  // Simplified scoring logic
  // Real implementation would use more sophisticated analysis

  let raw = 50
  let notes = ''

  switch (dimensionName) {
    case 'UX Flow Efficiency':
      raw = comparison.uxComparison.accessibilityComparison.scoreAfter
      notes = `Based on accessibility score of ${raw}`
      break

    case 'Code Quality':
      const complexity = comparison.technicalComparison.codeComplexity
      if (complexity.complexityChange === 'simpler') {
        raw = 80
        notes = 'Code complexity reduced'
      } else if (complexity.complexityChange === 'more-complex') {
        raw = 40
        notes = 'Code complexity increased'
      } else {
        raw = 60
        notes = 'Code complexity unchanged'
      }
      break

    default:
      raw = 50
      notes = 'No specific data available'
  }

  return {
    raw,
    normalized: Math.min(100, Math.max(0, raw)),
    notes
  }
}

