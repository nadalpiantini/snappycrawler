// REPORT GENERATOR
// ============================================

export function generateCompareReport(comparison: SnapshotComparison): string {
  const { meta, visualDiff, uxComparison, opportunities, recommendations } = comparison

  return `
# Snapshot Comparison Report

**Generated:** ${meta.comparedAt}
**Snapshots Compared:** ${meta.snapshotCount}

## URLs Compared

${meta.urls.map((url, i) => `${i + 1}. ${meta.names[i]}: ${url}`).join('\n')}

## Summary

**Total Differences:** ${meta.totalDifferences}
**Major Changes:** ${meta.majorChanges.length}

### Major Changes

${meta.majorChanges.length > 0 ?
  meta.majorChanges.map(change =>
    `### ${change.type.toUpperCase()}: ${change.description}\n**Impact:** ${change.impact}`
  ).join('\n\n') :
  'No major changes detected.'
}

---

## Visual Changes

**Total Changes:** ${visualDiff.summary.totalChanges}
- High Impact: ${visualDiff.summary.highImpact}
- Medium Impact: ${visualDiff.summary.mediumImpact}
- Low Impact: ${visualDiff.summary.lowImpact}

### Categories Changed
${visualDiff.summary.categoriesChanged.map(c => `- ${c}`).join('\n') || 'None'}

### Layout Changes
${visualDiff.layoutChanges.length > 0 ?
  visualDiff.layoutChanges.map(change =>
    `- **${change.type}:** ${change.description} (${change.impact} impact)`
  ).join('\n') :
  'No layout changes detected.'
}

### Component Changes
${visualDiff.componentChanges.length > 0 ?
  visualDiff.componentChanges.map(change =>
    `- **${change.component}:** ${change.action} - ${change.description}`
  ).join('\n') :
  'No component changes detected.'
}

---

## UX Comparison

### Flow Changes
${uxComparison.flowChanges.length > 0 ?
  uxComparison.flowChanges.map(flow =>
    `- **${flow.flowName}:** ${flow.action} (${flow.stepsBefore} → ${flow.stepsAfter} steps)`
  ).join('\n') :
  'No flow changes detected.'
}

### Accessibility
**Before:** ${uxComparison.accessibilityComparison.scoreBefore}/100
**After:** ${uxComparison.accessibilityComparison.scoreAfter}/100

${uxComparison.accessibilityComparison.improvements.length > 0 ?
  '**Improvements:**\n' + uxComparison.accessibilityComparison.improvements.map(i => `- ${i}`).join('\n') : ''
}
${uxComparison.accessibilityComparison.regressions.length > 0 ?
  '**Regressions:**\n' + uxComparison.accessibilityComparison.regressions.map(r => `- ${r}`).join('\n') : ''
}

### Usability
- **Complexity:** ${uxComparison.usabilityComparison.complexityBefore} → ${uxComparison.usabilityComparison.complexityAfter}
- **Cognitive Load:** ${uxComparison.usabilityComparison.cognitiveLoad}

---

## Opportunities

${opportunities.length > 0 ?
  opportunities.map((opp, i) =>
    `### ${i + 1}. ${opp.title}

**Category:** ${opp.category}
**Impact:** ${opp.impact} | **Effort:** ${opp.effort} | **Priority:** ${opp.priority}

${opp.description}
  `
  ).join('\n\n') :
  'No opportunities identified.'
}

---

## Recommendations

${recommendations.length > 0 ?
  recommendations.map((rec, i) =>
    `### ${i + 1}. ${rec.title}

**Type:** ${rec.type}
**Impact:** ${rec.impact} | **Effort:** ${rec.effort}

${rec.description}

**Rationale:** ${rec.rationale}
  `
  ).join('\n\n') :
  'No recommendations at this time.'
}

---

*Generated by SnappyCrawler Compare Mode*
  `.trim()
}

