import {
  SnapshotComparison,
  ComparisonMatrix,
  ComparisonDimension,
  ComparisonScore
} from './types'

// ============================================
// MAIN GENERATOR EXPORT
// ============================================

export async function generateAllOutputs(
  comparison: SnapshotComparison
): Promise<{
  report: string
  matrix: ComparisonMatrix
}> {
  return {
    report: generateCompareReport(comparison),
    matrix: generateComparisonMatrix(comparison)
  }
}

